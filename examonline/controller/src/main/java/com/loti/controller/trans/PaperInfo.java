package com.loti.controller.trans;

public class PaperInfo {
    int paperId;
    String paperName;
    int question0Num;
    int question1Num;
    int question2Num;
    int question4Num;
    int paperScore;

    public int getPaperId() {
        return paperId;
    }

    public void setPaperId(int paperId) {
        this.paperId = paperId;
    }

    public String getPaperName() {
        return paperName;
    }

    public void setPaperName(String paperName) {
        this.paperName = paperName;
    }

    public int getQuestion0Num() {
        return question0Num;
    }

    public void setQuestion0Num(int question0Num) {
        this.question0Num = question0Num;
    }

    public int getQuestion1Num() {
        return question1Num;
    }

    public void setQuestion1Num(int question1Num) {
        this.question1Num = question1Num;
    }

    public int getQuestion2Num() {
        return question2Num;
    }

    public void setQuestion2Num(int question2Num) {
        this.question2Num = question2Num;
    }

    public int getQuestion4Num() {
        return question4Num;
    }

    public void setQuestion4Num(int question4Num) {
        this.question4Num = question4Num;
    }

    public int getPaperScore() {
        return paperScore;
    }

    public void setPaperScore(int paperScore) {
        this.paperScore = paperScore;
    }
}
